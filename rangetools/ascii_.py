
from .base import BaseRange

# ------------------------------------------------------------------------

__all__ = [
    'AsciiRange',
]

# ------------------------------------------------------------------------
class AsciiRange(BaseRange):

    # ------------------------------------------------------------------------
    def to_num(self, value):
        return ord(value)
       
    # ------------------------------------------------------------------------
    def to_value(self, num):
        return chr(num)

    # ------------------------------------------------------------------------
    def step_to_num(self, value):
        return int(value)

    # ------------------------------------------------------------------------
    def step_to_value(self, num):
        return num

